// Функция анимации
function playFlowerAnimation() {
    let currentFrame = 1; // Начинаем со второго кадра
    
    function showNextFrame() {
        if (currentFrame < flowerFrames.length) {
            // Показываем текущий кадр
            flowerElement.textContent = flowerFrames[currentFrame];
            currentFrame++;
            
            // Ждем и показываем следующий кадр
            setTimeout(showNextFrame, 300); // 300ms между кадрами
        } else {
            // Анимация завершена - переходим на другую страницу
            window.location.href = 'second.html';
        }
    }
    
    // Запускаем анимацию
    showNextFrame();
}

